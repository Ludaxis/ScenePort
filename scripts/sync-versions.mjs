#!/usr/bin/env node
// Single-source the ScenePort version. Source of truth: the MCP server package.json.
// Usage:
//   node scripts/sync-versions.mjs           rewrite all derived files
//   node scripts/sync-versions.mjs --check    exit 1 if any file is out of sync
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), "..");
const check = process.argv.includes("--check");

const sourcePath = join(repoRoot, "plugins/sceneport/server/package.json");
const version = JSON.parse(readFileSync(sourcePath, "utf8")).version;
if (!/^\d+\.\d+\.\d+/.test(version)) {
  console.error(`Invalid source version "${version}" in ${sourcePath}`);
  process.exit(1);
}

// Each target: a file, a function that reads the current version, and one that sets it.
const jsonTargets = [
  {
    path: "plugins/sceneport/unity-package/package.json",
    get: (o) => o.version,
    set: (o) => (o.version = version),
  },
  {
    path: "plugins/sceneport/.claude-plugin/plugin.json",
    get: (o) => o.version,
    set: (o) => (o.version = version),
  },
  {
    path: "plugins/sceneport/.codex-plugin/plugin.json",
    get: (o) => o.version,
    set: (o) => (o.version = version),
  },
  {
    path: ".claude-plugin/marketplace.json",
    get: (o) => o.plugins?.find((p) => p.name === "sceneport")?.version,
    set: (o) => {
      const plugin = o.plugins?.find((p) => p.name === "sceneport");
      if (plugin) plugin.version = version;
    },
  },
];

const versionTsPath = "plugins/sceneport/server/src/version.ts";
const versionTs = `// Generated by scripts/sync-versions.mjs — do not edit by hand.\nexport const VERSION = "${version}";\n`;

const outOfSync = [];

for (const target of jsonTargets) {
  const abs = join(repoRoot, target.path);
  const obj = JSON.parse(readFileSync(abs, "utf8"));
  if (target.get(obj) === version) continue;

  if (check) {
    outOfSync.push(`${target.path} (has ${target.get(obj)}, expected ${version})`);
    continue;
  }
  target.set(obj);
  writeFileSync(abs, `${JSON.stringify(obj, null, 2)}\n`);
  console.log(`updated ${target.path} -> ${version}`);
}

const versionTsAbs = join(repoRoot, versionTsPath);
const currentTs = readFileSync(versionTsAbs, "utf8");
if (currentTs !== versionTs) {
  if (check) {
    outOfSync.push(versionTsPath);
  } else {
    writeFileSync(versionTsAbs, versionTs);
    console.log(`updated ${versionTsPath} -> ${version}`);
  }
}

if (check) {
  if (outOfSync.length > 0) {
    console.error(`Version out of sync (source ${version}):`);
    for (const item of outOfSync) console.error(`  - ${item}`);
    console.error("Run: node scripts/sync-versions.mjs");
    process.exit(1);
  }
  console.log(`All versions in sync at ${version}.`);
} else {
  console.log(`Synced to ${version}.`);
}
